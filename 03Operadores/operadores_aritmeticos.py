a = 10
b = 3

suma = a + b #Operador suma
resta = a - b #Operador resta
multiplicacion = a * b #Operador para multiplicar
division = a / b #Operador para dividir resulta en float
division2 = a // b #Operador para dividir resulta en int
modulo = a % b #residuo de división
exponente = a ** b #Operador para exponente

print(f'El resultado de la suma es: {suma}')
print(f'El resultado de la resta es: {resta}')
print(f'El resultado de la multiplicación es: {multiplicacion}')
print(f'El resultado de la división es: {division:.2f}') #:.2f se limita a 2 dígitos decimales
print(f'El resultado de la división entera es: {division2}')
print(f'El residuo de la división es: {modulo}')
print(f'El numero a elevado al número b es igual a: {exponente}')
