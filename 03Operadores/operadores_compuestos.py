a, b = 10, 15
print(f'Valor inicial a = {a}, b = {b}')

a += b #a = a + b (10 + 15) Operador compuesto de suma
print(f'Resultado de suma = {a}')
a=10
a -= b #a = a - b (10 - 15) Operador compuesto de resta
print(f'Resultado de resta = {a}')
a=10
a *= b #a = a*b (10*15) Operador compuesto de multiplicación
print(f'Resultado de multiplicacion = {a}')

a=10
a /= b #a = a/b (10/15) Operador compuesto de división
print(f'Resultado de division = {a:.2f}')